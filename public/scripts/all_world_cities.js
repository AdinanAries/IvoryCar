function all_world_cities_auto_complete(input_string){
    let counter = 0;

    //filter
    let cities_arr =  all_world_cities.filter(each => {
        return (
            each.name.replaceAll(" ", "").toLowerCase().includes(input_string.replaceAll(" ", "").replaceAll(",", "").toLowerCase()) ||
            each.country.replaceAll(" ", "").toLowerCase().includes(input_string.replaceAll("", "").replaceAll(",", "").toLowerCase()) ||
            (each.name + each.country).replaceAll(" ", "").toLowerCase().includes(input_string.replaceAll(",", "").replaceAll(" ", "").toLowerCase()) ||
            (each.country + each.name).replaceAll(" ", "").toLowerCase().includes(input_string.replaceAll(",", "").replaceAll(" ", "").toLowerCase())
        );
    });

    //reduce
    let reduced_arr = cities_arr.map(each => {
        counter++
        return counter < 6 && each;
    });

    //clean
    return reduced_arr.filter(each => each !== false);
}

function return_dom_for_all_wordl_cities(cities_arr){

    let dom_arr = cities_arr.map(each =>{

        return `
        <li onclick="pick_selected_city_from_all_world_cities_autocomplete('${each.name.replaceAll('\'', '#$#$%$#098')}','${each.country.replaceAll('\'', '#$#$%$#098')}');" style="cursor: pointer; padding: 5px 10px; overflow: initial !important;" >
            <p style="font-size: 14px; color:rgb(8, 77, 122); overflow: initial !important;">
            <i style="margin-right: 5px; color: rgb(235, 86, 0);" class="fa fa-map-marker" aria-hidden="true"></i>
            ${each.name}, ${each.country}</p>
        </li>
        `;

    });

    return dom_arr.join(' ');
}
